import { type NextPage } from "next";
import Head from "next/head";
import dynamic, { type DynamicOptions } from "next/dynamic";
import {
  ApplicationSettingsProvider,
  type ApplicationSettings,
} from "~/providers";
import { getSettingsConstants } from "~/server/api/config";

const DynamicRoom = dynamic(
  import("~/components/room/room.tsx") as DynamicOptions,
  {
    ssr: false,
  }
);

interface RoomPageProps {
  settings: ApplicationSettings;
}

const RoomPage: NextPage<RoomPageProps> = ({ settings }) => {
  return (
    <>
      <Head>
        <title>Chat Room</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <ApplicationSettingsProvider defaultStateOverride={settings}>
        <DynamicRoom />
      </ApplicationSettingsProvider>
    </>
  );
};

export function getServerSideProps() {
  const settings = getSettingsConstants();
  return { props: { settings: settings } };
}

export default RoomPage;
